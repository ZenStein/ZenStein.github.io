What are methods? Why are they useful?
methods are function that can be called, which run 'snippets' of code that can be passed variable values.  This is usefull for various reasons, some of which are, keeping code organized, clean and concise.  As well as being able to re use code for redundant evaluations and tasks.
What are local variables? Where are they able to be accessed (HINT: try accessing a local variable outside of a method to see).
local variables are variables that are available to you in your current scope. They are able to be accessed while in the scope of when they were defined, and although they cannot be accessed within a method, their value can be referenced and passed in as an argument to a method.  As long as that variable is available to you in the scope.
